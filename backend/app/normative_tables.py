AGE_GROUPS = [
    "8.7-9.0",
    "9.1-9.6",
    "9.7-10.6",
    "10.7-11.6",
    "11.7-13.0",
    "13.1-15.0",
    "15.1-18.0",
    "18+",
]


def _parse_norms() -> Dict[str, Dict[int, int]]:
    """
    Парсим текст, распределяя пары (raw, iq) по группам слева направо.
    Если пара дублируется, первое значение сохраняется.
    """
    norms: Dict[str, Dict[int, int]] = {g: {} for g in AGE_GROUPS}

    for line in RAW_TEXT.splitlines():
        tokens = re.findall(r"\d+", line)
        if not tokens:
            continue
        pairs = []
        for i in range(0, len(tokens) - 1, 2):
            try:
                raw = int(tokens[i])
                iq = int(tokens[i + 1])
                pairs.append((raw, iq))
            except ValueError:
                continue

        for idx, (raw, iq) in enumerate(pairs):
            group = AGE_GROUPS[idx % len(AGE_GROUPS)]
            norms[group].setdefault(raw, iq)

    return norms


NORMS = _parse_norms()


def get_age_group_label(age: int) -> str:
    """Определяем возрастную группу по возрасту в годах."""
    if age < 9:
        return "8.7-9.0"
    if age < 9.7:
        return "9.1-9.6"
    if age < 10.7:
        return "9.7-10.6"
    if age < 11.7:
        return "10.7-11.6"
    if age < 13.1:
        return "11.7-13.0"
    if age < 15.1:
        return "13.1-15.0"
    if age < 18.1:
        return "15.1-18.0"
    return "18+"


def get_iq_by_age(raw_score: int, age: int) -> Optional[Tuple[int, str]]:
    """
    Возвращает (iq, age_group). Если точного значения нет, берём ближайшее по
    минимальной разнице сырого балла.
    """
    age_group = get_age_group_label(age)
    mapping = NORMS.get(age_group, {})
    if not mapping:
        return None
    if raw_score in mapping:
        return mapping[raw_score], age_group

    # Находим ближайший по модулю
    closest_raw = min(mapping.keys(), key=lambda k: abs(k - raw_score))
    return mapping[closest_raw], age_group

